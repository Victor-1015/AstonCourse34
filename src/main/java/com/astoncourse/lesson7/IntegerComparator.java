package com.astoncourse.lesson7;

public class IntegerComparator {
    public static boolean areEqual(int a, int b) {
        return a == b;
    }

    public static boolean isGreater(int a, int b) {
        return a > b;
    }

    public static boolean isLess(int a, int b) {
        return a < b;
    }
}
